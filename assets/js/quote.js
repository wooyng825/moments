function randomQuote() {
    const quoteTheme = quotes[Math.floor(Math.random() * quotes.length)]
    const todayQuote = quoteTheme[Math.floor(Math.random() * quoteTheme.length)]

    quote.id = 'quote';
    author.id = 'author';
    quote.innerText = todayQuote.quote;
    author.innerText = `- ${todayQuote.author} -`;
    quoteSection.appendChild(quote);
    quoteSection.appendChild(br);
    quoteSection.appendChild(author);
}

